
def suma(n1: int, n2: int)-> int:
    """
    Suma dos números enteros.

    Args:
        n1 (int): Primer número entero.
        n2 (int): Segundo número entero.

    Returns:
        int: Resultado de la suma entre `n1` y `n2`.
    """
    return n1 + n2

def diferencia(n1, n2): 
    return n1 - n2

def producto(n1, n2):
    return n1 * n2